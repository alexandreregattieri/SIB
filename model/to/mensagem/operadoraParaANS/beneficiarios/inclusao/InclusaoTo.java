package model.to.mensagem.operadoraParaANS.beneficiarios.inclusao;

public class InclusaoTo {

	private IdentificacaoTo identificacao;
	private EnderecoTo endereco;
	private VinculoTo vinculo;
	
	public IdentificacaoTo getIdentificacao() {
		return identificacao;
	}
	public void setIdentificacao(IdentificacaoTo identificacao) {
		this.identificacao = identificacao;
	}
	public EnderecoTo getEndereco() {
		return endereco;
	}
	public void setEndereco(EnderecoTo endereco) {
		this.endereco = endereco;
	}
	public VinculoTo getVinculo() {
		return vinculo;
	}
	public void setVinculo(VinculoTo vinculo) {
		this.vinculo = vinculo;
	}
	
}

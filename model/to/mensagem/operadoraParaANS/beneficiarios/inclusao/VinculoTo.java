package model.to.mensagem.operadoraParaANS.beneficiarios.inclusao;

public class VinculoTo {

	private String codigoBeneficiario;
	private String relacaoDependencia;
	private String codigoBeneficiarioTitular;
	private String dataContratacao;
	private String numeroPlanoOperadora;
	
	public String getCodigoBeneficiario() {
		return codigoBeneficiario;
	}
	public void setCodigoBeneficiario(String codigoBeneficiario) {
		this.codigoBeneficiario = codigoBeneficiario;
	}
	public String getRelacaoDependencia() {
		return relacaoDependencia;
	}
	public void setRelacaoDependencia(String relacaoDependencia) {
		this.relacaoDependencia = relacaoDependencia;
	}
	public String getCodigoBeneficiarioTitular() {
		return codigoBeneficiarioTitular;
	}
	public void setCodigoBeneficiarioTitular(String codigoBeneficiarioTitular) {
		this.codigoBeneficiarioTitular = codigoBeneficiarioTitular;
	}
	public String getDataContratacao() {
		return dataContratacao;
	}
	public void setDataContratacao(String dataContratacao) {
		this.dataContratacao = dataContratacao;
	}
	public String getNumeroPlanoOperadora() {
		return numeroPlanoOperadora;
	}
	public void setNumeroPlanoOperadora(String numeroPlanoOperadora) {
		this.numeroPlanoOperadora = numeroPlanoOperadora;
	}
	
}
